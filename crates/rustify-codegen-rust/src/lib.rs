use rustify_ir::{
    BinaryOperator, Expression, ExpressionKind, Program, Statement, StringMethod, StringTransform,
    Type, UnaryOperator, Workspace,
};
use std::collections::HashSet;
use std::fmt::Write;
use thiserror::Error;

#[derive(Debug, Error)]
pub enum CodegenError {
    #[error("invalid structured IR: {0}")]
    InvalidIr(String),
}

pub fn emit(program: &Program) -> Result<String, CodegenError> {
    let mut output = String::from("// Generated by Rustify. Do not edit manually.\n\n");
    emit_declarations(&mut output, program, None, "")?;
    Ok(output)
}

pub fn emit_workspace(workspace: &Workspace) -> Result<String, CodegenError> {
    let mut output = String::from("// Generated by Rustify. Do not edit manually.\n\n");
    for module in &workspace.modules {
        writeln!(output, "#[allow(dead_code, unused_imports)]").unwrap();
        writeln!(output, "mod {} {{", module.name).unwrap();
        for import in &module.imports {
            let mut symbols = import
                .types
                .iter()
                .map(|binding| {
                    rust_import_binding(&binding.imported, &binding.local, rust_type_identifier)
                })
                .chain(import.values.iter().map(|binding| {
                    rust_import_binding(&binding.imported, &binding.local, rust_identifier)
                }))
                .collect::<Vec<_>>();
            symbols.sort();
            symbols.dedup();
            if !symbols.is_empty() {
                writeln!(
                    output,
                    "    use super::{}::{{{}}};",
                    import.module,
                    symbols.join(", ")
                )
                .unwrap();
            }
        }
        for reexport in &module.reexports {
            let mut symbols = reexport
                .types
                .iter()
                .map(|binding| {
                    rust_import_binding(&binding.imported, &binding.local, rust_type_identifier)
                })
                .chain(reexport.values.iter().map(|binding| {
                    rust_import_binding(&binding.imported, &binding.local, rust_identifier)
                }))
                .collect::<Vec<_>>();
            symbols.sort();
            symbols.dedup();
            if !symbols.is_empty() {
                writeln!(
                    output,
                    "    pub use super::{}::{{{}}};",
                    reexport.module,
                    symbols.join(", ")
                )
                .unwrap();
            }
        }
        if !module.imports.is_empty() || !module.reexports.is_empty() {
            output.push('\n');
        }
        let mut exports: HashSet<_> = module.exports.iter().map(String::as_str).collect();
        if let Some(default_export) = &module.default_export {
            exports.insert(default_export);
        }
        let mut body = String::new();
        emit_declarations(
            &mut body,
            &module.program,
            Some(&exports),
            &module.source_path,
        )?;
        for line in body.lines() {
            if line.is_empty() {
                output.push('\n');
            } else {
                writeln!(output, "    {line}").unwrap();
            }
        }
        if let Some(default_export) = &module.default_export {
            let (symbol, alias) = module_default_export(&module.program, default_export)?;
            writeln!(output, "    pub use self::{symbol} as {alias};\n").unwrap();
        }
        writeln!(output, "}}\n").unwrap();
    }

    let entry = workspace
        .modules
        .iter()
        .find(|module| module.name == workspace.entry)
        .ok_or_else(|| CodegenError::InvalidIr("workspace entry module is missing".to_owned()))?;
    if !entry.exports.is_empty() || entry.default_export.is_some() {
        writeln!(output, "pub use {}::*;", workspace.entry).unwrap();
    }
    Ok(output)
}

fn module_default_export(program: &Program, name: &str) -> Result<(String, String), CodegenError> {
    if program.structs.iter().any(|item| item.name == name)
        || program.enums.iter().any(|item| item.name == name)
    {
        Ok((rust_type_identifier(name), rust_type_identifier("default")))
    } else if program.functions.iter().any(|item| item.name == name) {
        Ok((rust_identifier(name), rust_identifier("default")))
    } else {
        Err(CodegenError::InvalidIr(format!(
            "default export `{name}` is not declared in its module"
        )))
    }
}

fn rust_import_binding(imported: &str, local: &str, normalize: fn(&str) -> String) -> String {
    let imported = normalize(imported);
    let local = normalize(local);
    if imported == local {
        imported
    } else {
        format!("{imported} as {local}")
    }
}

fn emit_declarations(
    output: &mut String,
    program: &Program,
    exports: Option<&HashSet<&str>>,
    source_path: &str,
) -> Result<(), CodegenError> {
    for structure in &program.structs {
        writeln!(output, "#[derive(Clone, Debug, PartialEq)]").unwrap();
        writeln!(
            output,
            "{}struct {} {{",
            visibility(&structure.name, exports),
            rust_type_identifier(&structure.name)
        )
        .unwrap();
        for field in &structure.fields {
            writeln!(
                output,
                "    pub {}: {},",
                rust_identifier(&field.name),
                emit_type(&field.ty)
            )
            .unwrap();
        }
        writeln!(output, "}}\n").unwrap();
    }
    for enumeration in &program.enums {
        writeln!(output, "#[derive(Clone, Debug, PartialEq)]").unwrap();
        writeln!(
            output,
            "{}enum {} {{",
            visibility(&enumeration.name, exports),
            rust_type_identifier(&enumeration.name)
        )
        .unwrap();
        for variant in &enumeration.variants {
            writeln!(output, "    {},", rust_type_identifier(variant)).unwrap();
        }
        writeln!(output, "}}\n").unwrap();
    }
    for function in &program.functions {
        write!(
            output,
            "{}{}fn {}(",
            visibility(&function.name, exports),
            if function.is_async { "async " } else { "" },
            rust_identifier(&function.name)
        )
        .unwrap();
        for (index, parameter) in function.params.iter().enumerate() {
            if index > 0 {
                output.push_str(", ");
            }
            write!(
                output,
                "{}: {}",
                rust_identifier(&parameter.name),
                emit_type(&parameter.ty)
            )
            .unwrap();
        }
        if function.return_type == Type::Unit {
            writeln!(output, ") {{").unwrap();
        } else {
            writeln!(output, ") -> {} {{", emit_type(&function.return_type)).unwrap();
        }

        if function.is_hybrid {
            let mut args = Vec::new();
            for param in &function.params {
                args.push(format!(
                    "serde_json::json!({})",
                    rust_identifier(&param.name)
                ));
            }
            if function.return_type == Type::Unit {
                writeln!(
                    output,
                    "    let _: () = rustify_runtime::call_js_fallback(\"{}\", \"{}\", &[{}]).unwrap();",
                    source_path,
                    function.name,
                    args.join(", ")
                )
                .unwrap();
            } else {
                writeln!(
                    output,
                    "    rustify_runtime::call_js_fallback(\"{}\", \"{}\", &[{}]).unwrap()",
                    source_path,
                    function.name,
                    args.join(", ")
                )
                .unwrap();
            }
        } else {
            for parameter in &function.params {
                if !statements_use_identifier(&function.body, &parameter.name) {
                    writeln!(output, "    let _ = &{};", rust_identifier(&parameter.name)).unwrap();
                }
            }
            emit_statements(output, &function.body, 1)?;
        }
        writeln!(output, "}}\n").unwrap();
    }
    Ok(())
}

fn visibility(name: &str, exports: Option<&HashSet<&str>>) -> &'static str {
    if exports.is_none_or(|exports| exports.contains(name)) {
        "pub "
    } else {
        ""
    }
}

pub fn uses_runtime(program: &Program) -> bool {
    program
        .structs
        .iter()
        .flat_map(|structure| structure.fields.iter())
        .any(|field| type_uses_runtime(&field.ty))
        || program.functions.iter().any(|function| {
            function.is_hybrid
                || type_uses_runtime(&function.return_type)
                || function
                    .params
                    .iter()
                    .any(|parameter| type_uses_runtime(&parameter.ty))
                || statements_use_runtime(&function.body)
        })
}

pub fn workspace_uses_runtime(workspace: &Workspace) -> bool {
    workspace
        .modules
        .iter()
        .any(|module| uses_runtime(&module.program))
}

fn type_uses_runtime(ty: &Type) -> bool {
    match ty {
        Type::JsonValue => true,
        Type::Vec(inner) | Type::Option(inner) | Type::Promise(inner) => type_uses_runtime(inner),
        Type::Result(ok, error) => type_uses_runtime(ok) || type_uses_runtime(error),
        _ => false,
    }
}

fn statements_use_runtime(statements: &[Statement]) -> bool {
    statements.iter().any(|statement| match statement {
        Statement::Variable { value, .. }
        | Statement::Assignment { value, .. }
        | Statement::Return(value)
        | Statement::Expression(value) => expression_uses_runtime(value),
        Statement::ConsoleLog(values) => values.iter().any(expression_uses_runtime),
        Statement::If {
            condition,
            then_body,
            else_body,
        } => {
            expression_uses_runtime(condition)
                || statements_use_runtime(then_body)
                || statements_use_runtime(else_body)
        }
        Statement::While { condition, body } => {
            expression_uses_runtime(condition) || statements_use_runtime(body)
        }
        Statement::ForOf { iterable, body, .. } => {
            expression_uses_runtime(iterable) || statements_use_runtime(body)
        }
        Statement::ReturnVoid | Statement::Break | Statement::Continue => false,
    })
}

fn statements_use_identifier(statements: &[Statement], name: &str) -> bool {
    for statement in statements {
        if statement_uses_identifier(statement, name) {
            return true;
        }

        if matches!(statement, Statement::Variable { name: binding, .. } if binding == name) {
            return false;
        }

        if statement_definitely_exits(statement) {
            return false;
        }
    }

    false
}

fn statement_uses_identifier(statement: &Statement, name: &str) -> bool {
    match statement {
        Statement::Variable { value, .. }
        | Statement::Assignment { value, .. }
        | Statement::Return(value)
        | Statement::Expression(value) => expression_uses_identifier(value, name),
        Statement::ConsoleLog(values) => values
            .iter()
            .any(|value| expression_uses_identifier(value, name)),
        Statement::If {
            condition,
            then_body,
            else_body,
        } => {
            expression_uses_identifier(condition, name)
                || statements_use_identifier(then_body, name)
                || statements_use_identifier(else_body, name)
        }
        Statement::While { condition, body } => {
            expression_uses_identifier(condition, name) || statements_use_identifier(body, name)
        }
        Statement::ForOf {
            binding,
            iterable,
            body,
        } => {
            expression_uses_identifier(iterable, name)
                || (binding != name && statements_use_identifier(body, name))
        }
        Statement::ReturnVoid | Statement::Break | Statement::Continue => false,
    }
}

fn statements_mutate_identifier(statements: &[Statement], name: &str) -> bool {
    for statement in statements {
        if statement_mutates_identifier(statement, name) {
            return true;
        }

        if matches!(statement, Statement::Variable { name: binding, .. } if binding == name) {
            return false;
        }

        if statement_definitely_exits(statement) {
            return false;
        }
    }

    false
}

fn statement_definitely_exits(statement: &Statement) -> bool {
    match statement {
        Statement::Return(_) | Statement::ReturnVoid | Statement::Break | Statement::Continue => {
            true
        }
        Statement::If {
            then_body,
            else_body,
            ..
        } => {
            !else_body.is_empty()
                && then_body.last().is_some_and(statement_definitely_exits)
                && else_body.last().is_some_and(statement_definitely_exits)
        }
        _ => false,
    }
}

fn statement_mutates_identifier(statement: &Statement, name: &str) -> bool {
    match statement {
        Statement::Assignment {
            name: assignment,
            value,
        } => assignment == name || expression_mutates_identifier(value, name),
        Statement::Variable { value, .. }
        | Statement::Return(value)
        | Statement::Expression(value) => expression_mutates_identifier(value, name),
        Statement::ConsoleLog(values) => values
            .iter()
            .any(|value| expression_mutates_identifier(value, name)),
        Statement::If {
            condition,
            then_body,
            else_body,
        } => {
            expression_mutates_identifier(condition, name)
                || statements_mutate_identifier(then_body, name)
                || statements_mutate_identifier(else_body, name)
        }
        Statement::While { condition, body } => {
            expression_mutates_identifier(condition, name)
                || statements_mutate_identifier(body, name)
        }
        Statement::ForOf {
            binding,
            iterable,
            body,
        } => {
            expression_mutates_identifier(iterable, name)
                || (binding != name && statements_mutate_identifier(body, name))
        }
        Statement::ReturnVoid | Statement::Break | Statement::Continue => false,
    }
}

fn expression_mutates_identifier(expression: &Expression, name: &str) -> bool {
    match &expression.kind {
        ExpressionKind::ArrayPush { array, value } => {
            matches!(&array.kind, ExpressionKind::Identifier(identifier) if identifier == name)
                || expression_mutates_identifier(value, name)
        }
        ExpressionKind::ArrayPop(array) => {
            matches!(&array.kind, ExpressionKind::Identifier(identifier) if identifier == name)
        }
        ExpressionKind::Some(value)
        | ExpressionKind::Await(value)
        | ExpressionKind::ArrayLength(value)
        | ExpressionKind::OptionCheck { value, .. }
        | ExpressionKind::ResultCheck { value, .. }
        | ExpressionKind::StringTransform {
            receiver: value, ..
        }
        | ExpressionKind::Property { object: value, .. }
        | ExpressionKind::Unary { value, .. } => expression_mutates_identifier(value, name),
        ExpressionKind::ArrayIncludes { array, value } => {
            expression_mutates_identifier(array, name) || expression_mutates_identifier(value, name)
        }
        ExpressionKind::ArrayGet { array, index } => {
            expression_mutates_identifier(array, name) || expression_mutates_identifier(index, name)
        }
        ExpressionKind::ArrayJoin { array, separator } => {
            expression_mutates_identifier(array, name)
                || expression_mutates_identifier(separator, name)
        }
        ExpressionKind::OptionUnwrapOr { value, fallback }
        | ExpressionKind::ResultUnwrapOr { value, fallback } => {
            expression_mutates_identifier(value, name)
                || expression_mutates_identifier(fallback, name)
        }
        ExpressionKind::StringMethod {
            receiver, argument, ..
        } => {
            expression_mutates_identifier(receiver, name)
                || expression_mutates_identifier(argument, name)
        }
        ExpressionKind::Array(values) => values
            .iter()
            .any(|value| expression_mutates_identifier(value, name)),
        ExpressionKind::StructLiteral { fields, .. } => fields
            .iter()
            .any(|(_, value)| expression_mutates_identifier(value, name)),
        ExpressionKind::Call { arguments, .. } => arguments
            .iter()
            .any(|value| expression_mutates_identifier(value, name)),
        ExpressionKind::Binary { left, right, .. } => {
            expression_mutates_identifier(left, name) || expression_mutates_identifier(right, name)
        }
        ExpressionKind::Conditional {
            condition,
            then_value,
            else_value,
        } => {
            expression_mutates_identifier(condition, name)
                || expression_mutates_identifier(then_value, name)
                || expression_mutates_identifier(else_value, name)
        }
        ExpressionKind::Template { expressions, .. } => expressions
            .iter()
            .any(|value| expression_mutates_identifier(value, name)),
        ExpressionKind::String(_)
        | ExpressionKind::Number(_)
        | ExpressionKind::Boolean(_)
        | ExpressionKind::Null
        | ExpressionKind::Identifier(_)
        | ExpressionKind::EnumVariant { .. } => false,
    }
}

fn expression_uses_identifier(expression: &Expression, name: &str) -> bool {
    match &expression.kind {
        ExpressionKind::Identifier(identifier) => identifier == name,
        ExpressionKind::Some(value)
        | ExpressionKind::Await(value)
        | ExpressionKind::ArrayLength(value)
        | ExpressionKind::ArrayPop(value)
        | ExpressionKind::OptionCheck { value, .. }
        | ExpressionKind::ResultCheck { value, .. }
        | ExpressionKind::StringTransform {
            receiver: value, ..
        }
        | ExpressionKind::Property { object: value, .. }
        | ExpressionKind::Unary { value, .. } => expression_uses_identifier(value, name),
        ExpressionKind::ArrayIncludes { array, value }
        | ExpressionKind::ArrayPush { array, value } => {
            expression_uses_identifier(array, name) || expression_uses_identifier(value, name)
        }
        ExpressionKind::ArrayGet { array, index } => {
            expression_uses_identifier(array, name) || expression_uses_identifier(index, name)
        }
        ExpressionKind::ArrayJoin { array, separator } => {
            expression_uses_identifier(array, name) || expression_uses_identifier(separator, name)
        }
        ExpressionKind::OptionUnwrapOr { value, fallback }
        | ExpressionKind::ResultUnwrapOr { value, fallback } => {
            expression_uses_identifier(value, name) || expression_uses_identifier(fallback, name)
        }
        ExpressionKind::StringMethod {
            receiver, argument, ..
        } => {
            expression_uses_identifier(receiver, name) || expression_uses_identifier(argument, name)
        }
        ExpressionKind::Array(values) => values
            .iter()
            .any(|value| expression_uses_identifier(value, name)),
        ExpressionKind::StructLiteral { fields, .. } => fields
            .iter()
            .any(|(_, value)| expression_uses_identifier(value, name)),
        ExpressionKind::Call { arguments, .. } => arguments
            .iter()
            .any(|value| expression_uses_identifier(value, name)),
        ExpressionKind::Binary { left, right, .. } => {
            expression_uses_identifier(left, name) || expression_uses_identifier(right, name)
        }
        ExpressionKind::Conditional {
            condition,
            then_value,
            else_value,
        } => {
            expression_uses_identifier(condition, name)
                || expression_uses_identifier(then_value, name)
                || expression_uses_identifier(else_value, name)
        }
        ExpressionKind::Template { expressions, .. } => expressions
            .iter()
            .any(|value| expression_uses_identifier(value, name)),
        ExpressionKind::String(_)
        | ExpressionKind::Number(_)
        | ExpressionKind::Boolean(_)
        | ExpressionKind::Null
        | ExpressionKind::EnumVariant { .. } => false,
    }
}

fn expression_uses_runtime(expression: &Expression) -> bool {
    type_uses_runtime(&expression.ty)
        || match &expression.kind {
            ExpressionKind::Call { callee, arguments } => {
                callee.starts_with("JSON.")
                    || callee.starts_with("Rustify.")
                    || arguments.iter().any(expression_uses_runtime)
            }
            ExpressionKind::Some(value)
            | ExpressionKind::Await(value)
            | ExpressionKind::ArrayLength(value)
            | ExpressionKind::Property { object: value, .. } => expression_uses_runtime(value),
            ExpressionKind::ArrayIncludes { array, value } => {
                expression_uses_runtime(array) || expression_uses_runtime(value)
            }
            ExpressionKind::ArrayPush { array, value } => {
                expression_uses_runtime(array) || expression_uses_runtime(value)
            }
            ExpressionKind::ArrayPop(array) => expression_uses_runtime(array),
            ExpressionKind::ArrayGet { array, index } => {
                expression_uses_runtime(array) || expression_uses_runtime(index)
            }
            ExpressionKind::ArrayJoin { array, separator } => {
                expression_uses_runtime(array) || expression_uses_runtime(separator)
            }
            ExpressionKind::OptionCheck { value, .. } => expression_uses_runtime(value),
            ExpressionKind::OptionUnwrapOr { value, fallback } => {
                expression_uses_runtime(value) || expression_uses_runtime(fallback)
            }
            ExpressionKind::ResultCheck { value, .. } => expression_uses_runtime(value),
            ExpressionKind::ResultUnwrapOr { value, fallback } => {
                expression_uses_runtime(value) || expression_uses_runtime(fallback)
            }
            ExpressionKind::StringMethod {
                receiver, argument, ..
            } => expression_uses_runtime(receiver) || expression_uses_runtime(argument),
            ExpressionKind::StringTransform { receiver, .. } => expression_uses_runtime(receiver),
            ExpressionKind::Array(values) => values.iter().any(expression_uses_runtime),
            ExpressionKind::StructLiteral { fields, .. } => fields
                .iter()
                .any(|(_, value)| expression_uses_runtime(value)),
            ExpressionKind::Binary { left, right, .. } => {
                expression_uses_runtime(left) || expression_uses_runtime(right)
            }
            ExpressionKind::Unary { value, .. } => expression_uses_runtime(value),
            ExpressionKind::Conditional {
                condition,
                then_value,
                else_value,
            } => {
                expression_uses_runtime(condition)
                    || expression_uses_runtime(then_value)
                    || expression_uses_runtime(else_value)
            }
            ExpressionKind::Template { expressions, .. } => {
                expressions.iter().any(expression_uses_runtime)
            }
            _ => false,
        }
}

pub fn explain(program: &Program) -> String {
    let mut output = String::new();
    for structure in &program.structs {
        writeln!(
            output,
            "type {} -> Rust struct {}",
            structure.name,
            rust_type_identifier(&structure.name)
        )
        .unwrap();
    }
    for enumeration in &program.enums {
        writeln!(
            output,
            "enum {} -> Rust enum {}",
            enumeration.name,
            rust_type_identifier(&enumeration.name)
        )
        .unwrap();
    }
    for function in &program.functions {
        let params = function
            .params
            .iter()
            .map(|param| format!("{}: {}", rust_identifier(&param.name), emit_type(&param.ty)))
            .collect::<Vec<_>>()
            .join(", ");
        writeln!(
            output,
            "{}function {} -> pub {}fn {}({}) -> {} ({} statement(s))",
            if function.is_async { "async " } else { "" },
            function.name,
            if function.is_async { "async " } else { "" },
            rust_identifier(&function.name),
            params,
            emit_type(&function.return_type),
            function.body.len()
        )
        .unwrap();
        explain_statements(&mut output, &function.body, 1);
    }
    output
}

pub fn explain_workspace(workspace: &Workspace) -> String {
    let mut output = String::new();
    for module in &workspace.modules {
        writeln!(output, "module {}:", module.name).unwrap();
        for line in explain(&module.program).lines() {
            writeln!(output, "  {line}").unwrap();
        }
    }
    output
}

fn explain_statements(output: &mut String, statements: &[Statement], depth: usize) {
    let indent = "  ".repeat(depth);
    for statement in statements {
        let description = match statement {
            Statement::Variable {
                name,
                mutable,
                ty,
                value,
            } => format!(
                "{} variable `{name}: {}` from {}",
                if *mutable { "mutable" } else { "immutable" },
                emit_type(ty),
                explain_expression(value)
            ),
            Statement::Assignment { name, value } => {
                format!("assignment to `{name}` from {}", explain_expression(value))
            }
            Statement::Return(value) => format!(
                "return `{}` from {}",
                emit_type(&value.ty),
                explain_expression(value)
            ),
            Statement::ReturnVoid => "return void".to_owned(),
            Statement::Break => "break loop".to_owned(),
            Statement::Continue => "continue loop".to_owned(),
            Statement::Expression(value) => explain_expression(value),
            Statement::ConsoleLog(values) => {
                format!("console.log({} argument(s)) -> println!", values.len())
            }
            Statement::If { condition, .. } => {
                format!("if/else with {}", explain_expression(condition))
            }
            Statement::While { condition, .. } => {
                format!("while loop with {}", explain_expression(condition))
            }
            Statement::ForOf {
                binding, iterable, ..
            } => format!(
                "for...of -> Rust for binding `{binding}` over {}",
                explain_expression(iterable)
            ),
        };
        writeln!(output, "{indent}{description}").unwrap();
        match statement {
            Statement::If {
                then_body,
                else_body,
                ..
            } => {
                explain_statements(output, then_body, depth + 1);
                explain_statements(output, else_body, depth + 1);
            }
            Statement::While { body, .. } | Statement::ForOf { body, .. } => {
                explain_statements(output, body, depth + 1);
            }
            _ => {}
        }
    }
}

fn explain_expression(expression: &Expression) -> String {
    match &expression.kind {
        ExpressionKind::String(_) => "string literal -> String".to_owned(),
        ExpressionKind::Number(_) => "number literal -> f64".to_owned(),
        ExpressionKind::Boolean(_) => "boolean literal -> bool".to_owned(),
        ExpressionKind::Null => format!("null -> {}", emit_type(&expression.ty)),
        ExpressionKind::Some(value) => {
            format!("nullable value -> Some({})", explain_expression(value))
        }
        ExpressionKind::Await(value) => {
            format!("await -> Rust .await on {}", explain_expression(value))
        }
        ExpressionKind::Identifier(name) => format!("identifier `{name}`"),
        ExpressionKind::Array(values) => {
            format!("array literal -> Vec with {} element(s)", values.len())
        }
        ExpressionKind::StructLiteral { name, .. } => format!("object literal -> `{name}` struct"),
        ExpressionKind::Call { callee, .. } => {
            format!("call `{callee}` -> `{}`", rust_callee(callee))
        }
        ExpressionKind::Property { property, .. } => format!(
            "property access -> Rust field `{}`{}",
            rust_identifier(property),
            if type_is_copy(&expression.ty) {
                ""
            } else {
                " cloned to preserve reusable TypeScript access"
            }
        ),
        ExpressionKind::ArrayLength(_) => "array.length -> Vec::len() as f64".to_owned(),
        ExpressionKind::ArrayIncludes { .. } => "array.includes -> Vec::contains".to_owned(),
        ExpressionKind::ArrayPush { .. } => "array.push -> Vec::push".to_owned(),
        ExpressionKind::ArrayPop(_) => "array.pop -> Vec::pop returning Option<T>".to_owned(),
        ExpressionKind::ArrayGet { .. } => {
            "array[index] -> checked Vec::get(index).cloned() returning Option<T>".to_owned()
        }
        ExpressionKind::ArrayJoin { .. } => "array.join -> Vec<String>::join".to_owned(),
        ExpressionKind::OptionCheck { is_some, .. } => format!(
            "optional check -> Option::{}",
            if *is_some { "is_some" } else { "is_none" }
        ),
        ExpressionKind::OptionUnwrapOr { .. } => {
            "optional fallback -> Option::unwrap_or without panic".to_owned()
        }
        ExpressionKind::ResultCheck { is_ok, .. } => format!(
            "result check -> Result::{}",
            if *is_ok { "is_ok" } else { "is_err" }
        ),
        ExpressionKind::ResultUnwrapOr { .. } => {
            "result fallback -> Result::unwrap_or without panic".to_owned()
        }
        ExpressionKind::StringMethod { method, .. } => format!(
            "string method -> String::{}",
            match method {
                StringMethod::Includes => "contains",
                StringMethod::StartsWith => "starts_with",
                StringMethod::EndsWith => "ends_with",
            }
        ),
        ExpressionKind::StringTransform { transform, .. } => format!(
            "string transform -> String::{}",
            match transform {
                StringTransform::Trim => "trim",
                StringTransform::ToUpperCase => "to_uppercase",
                StringTransform::ToLowerCase => "to_lowercase",
            }
        ),
        ExpressionKind::EnumVariant {
            enumeration,
            variant,
        } => format!("enum variant -> {enumeration}::{variant}"),
        ExpressionKind::Binary { operator, .. } => {
            format!("binary operation `{}`", emit_operator(*operator))
        }
        ExpressionKind::Unary { operator, .. } => format!(
            "unary operation `{}`",
            match operator {
                UnaryOperator::Not => "!",
                UnaryOperator::Negate => "-",
            }
        ),
        ExpressionKind::Conditional { .. } => "conditional expression -> Rust if/else".to_owned(),
        ExpressionKind::Template { .. } => "template literal -> format!".to_owned(),
    }
}

pub fn emit_type(ty: &Type) -> String {
    match ty {
        Type::String => "String".to_owned(),
        Type::F64 => "f64".to_owned(),
        Type::Bool => "bool".to_owned(),
        Type::Unit => "()".to_owned(),
        Type::JsonValue => "rustify_runtime::JsonValue".to_owned(),
        Type::Named(name) => rust_type_identifier(name),
        Type::Vec(inner) => format!("Vec<{}>", emit_type(inner)),
        Type::Option(inner) => format!("Option<{}>", emit_type(inner)),
        Type::Result(ok, error) => format!("Result<{}, {}>", emit_type(ok), emit_type(error)),
        Type::Promise(inner) => format!("impl std::future::Future<Output = {}>", emit_type(inner)),
    }
}

fn type_is_copy(ty: &Type) -> bool {
    matches!(ty, Type::F64 | Type::Bool | Type::Unit)
}

fn type_requires_clone(ty: &Type) -> bool {
    !type_is_copy(ty) && !matches!(ty, Type::Promise(_))
}

fn type_is_must_use(ty: &Type) -> bool {
    matches!(ty, Type::Option(_) | Type::Result(_, _))
}

fn emit_mutable_receiver(expression: &Expression) -> Result<String, CodegenError> {
    match &expression.kind {
        ExpressionKind::Identifier(name) => Ok(rust_identifier(name)),
        _ => Err(CodegenError::InvalidIr(
            "array mutation receiver must be a local binding".to_owned(),
        )),
    }
}

fn emit_borrow_source(expression: &Expression) -> Result<String, CodegenError> {
    match &expression.kind {
        ExpressionKind::Identifier(name) => Ok(rust_identifier(name)),
        ExpressionKind::Property { object, property } => Ok(format!(
            "{}.{}",
            emit_borrow_source(object)?,
            rust_identifier(property)
        )),
        _ => emit_expression(expression),
    }
}

fn emit_statements(
    output: &mut String,
    statements: &[Statement],
    depth: usize,
) -> Result<(), CodegenError> {
    for (index, statement) in statements.iter().enumerate() {
        emit_statement(output, statement, &statements[index + 1..], depth)?;
        if statement_definitely_exits(statement) {
            break;
        }
    }
    Ok(())
}

fn emit_statement(
    output: &mut String,
    statement: &Statement,
    following: &[Statement],
    depth: usize,
) -> Result<(), CodegenError> {
    let indent = "    ".repeat(depth);
    match statement {
        Statement::Variable {
            name,
            mutable,
            ty,
            value,
        } => writeln!(
            output,
            "{indent}let {}{}: {} = {};",
            if *mutable && statements_mutate_identifier(following, name) {
                "mut "
            } else {
                ""
            },
            rust_identifier(name),
            emit_type(ty),
            emit_expression(value)?
        )
        .and_then(|_| writeln!(output, "{indent}let _ = &{};", rust_identifier(name)))
        .unwrap(),
        Statement::Assignment { name, value } => writeln!(
            output,
            "{indent}{} = {};",
            rust_identifier(name),
            emit_expression(value)?
        )
        .and_then(|_| writeln!(output, "{indent}let _ = &{};", rust_identifier(name)))
        .unwrap(),
        Statement::Return(value) => {
            writeln!(output, "{indent}return {};", emit_expression(value)?).unwrap()
        }
        Statement::ReturnVoid => writeln!(output, "{indent}return;").unwrap(),
        Statement::Break => writeln!(output, "{indent}break;").unwrap(),
        Statement::Continue => writeln!(output, "{indent}continue;").unwrap(),
        Statement::Expression(value) if type_is_must_use(&value.ty) => {
            writeln!(output, "{indent}let _ = {};", emit_expression(value)?).unwrap()
        }
        Statement::Expression(value) => {
            writeln!(output, "{indent}{};", emit_expression(value)?).unwrap()
        }
        Statement::ConsoleLog(values) => {
            if values.is_empty() {
                writeln!(output, "{indent}println!();").unwrap();
            } else {
                let format = values
                    .iter()
                    .map(|value| {
                        if matches!(value.kind, ExpressionKind::Template { .. }) {
                            "{}"
                        } else {
                            "{:?}"
                        }
                    })
                    .collect::<Vec<_>>()
                    .join(" ");
                let arguments = values
                    .iter()
                    .map(emit_borrow_source)
                    .collect::<Result<Vec<_>, _>>()?
                    .join(", ");
                writeln!(output, "{indent}println!(\"{format}\", {arguments});").unwrap();
            }
        }
        Statement::If {
            condition,
            then_body,
            else_body,
        } => {
            writeln!(output, "{indent}if {} {{", emit_expression(condition)?).unwrap();
            emit_statements(output, then_body, depth + 1)?;
            if else_body.is_empty() {
                writeln!(output, "{indent}}}").unwrap();
            } else {
                writeln!(output, "{indent}}} else {{").unwrap();
                emit_statements(output, else_body, depth + 1)?;
                writeln!(output, "{indent}}}").unwrap();
            }
        }
        Statement::While { condition, body } => {
            writeln!(output, "{indent}while {} {{", emit_expression(condition)?).unwrap();
            emit_statements(output, body, depth + 1)?;
            writeln!(output, "{indent}}}").unwrap();
        }
        Statement::ForOf {
            binding,
            iterable,
            body,
        } => {
            writeln!(
                output,
                "{indent}for {} in {} {{",
                rust_identifier(binding),
                emit_expression(iterable)?
            )
            .unwrap();
            if !statements_use_identifier(body, binding) {
                writeln!(output, "{indent}    let _ = &{};", rust_identifier(binding)).unwrap();
            }
            emit_statements(output, body, depth + 1)?;
            writeln!(output, "{indent}}}").unwrap();
        }
    }
    Ok(())
}

fn emit_expression(expression: &Expression) -> Result<String, CodegenError> {
    emit_expression_with_precedence(expression, 0)
}

fn emit_expression_with_precedence(
    expression: &Expression,
    parent_precedence: u8,
) -> Result<String, CodegenError> {
    Ok(match &expression.kind {
        ExpressionKind::String(value) => format!("{value:?}.to_string()"),
        ExpressionKind::Number(value) => {
            if value.fract() == 0.0 {
                format!("{value:.1}")
            } else {
                value.to_string()
            }
        }
        ExpressionKind::Boolean(value) => value.to_string(),
        ExpressionKind::Null => "None".to_owned(),
        ExpressionKind::Some(value) => format!("Some({})", emit_expression(value)?),
        ExpressionKind::Await(value) => format!("{}.await", emit_expression(value)?),
        ExpressionKind::Identifier(name) => {
            let identifier = rust_identifier(name);
            if type_requires_clone(&expression.ty) {
                format!("{identifier}.clone()")
            } else {
                identifier
            }
        }
        ExpressionKind::Array(values) => format!(
            "vec![{}]",
            values
                .iter()
                .map(emit_expression)
                .collect::<Result<Vec<_>, _>>()?
                .join(", ")
        ),
        ExpressionKind::StructLiteral { name, fields } => format!(
            "{} {{ {} }}",
            rust_type_identifier(name),
            fields
                .iter()
                .map(|(field, value)| {
                    Ok(format!(
                        "{}: {}",
                        rust_identifier(field),
                        emit_expression(value)?
                    ))
                })
                .collect::<Result<Vec<_>, CodegenError>>()?
                .join(", ")
        ),
        ExpressionKind::Call { callee, arguments } => format!(
            "{}({})",
            rust_callee(callee),
            arguments
                .iter()
                .map(emit_expression)
                .collect::<Result<Vec<_>, _>>()?
                .join(", ")
        ),
        ExpressionKind::Property { object, property } => {
            let access = format!(
                "{}.{}",
                emit_borrow_source(object)?,
                rust_identifier(property)
            );
            if type_is_copy(&expression.ty) {
                access
            } else {
                format!("{access}.clone()")
            }
        }
        ExpressionKind::ArrayLength(value) => {
            format!("{}.len() as f64", emit_borrow_source(value)?)
        }
        ExpressionKind::ArrayIncludes { array, value } => {
            format!(
                "{}.contains(&{})",
                emit_borrow_source(array)?,
                emit_borrow_source(value)?
            )
        }
        ExpressionKind::ArrayPush { array, value } => {
            format!(
                "{}.push({})",
                emit_mutable_receiver(array)?,
                emit_expression(value)?
            )
        }
        ExpressionKind::ArrayPop(array) => format!("{}.pop()", emit_mutable_receiver(array)?),
        ExpressionKind::ArrayGet { array, index } => format!(
            "{{ let rustify_array_ref = &{}; let rustify_index_value: f64 = {}; \
             if rustify_index_value >= 0.0 && rustify_index_value.fract() == 0.0 {{ \
             rustify_array_ref.get(rustify_index_value as usize).cloned() }} else {{ None }} }}",
            emit_borrow_source(array)?,
            emit_expression(index)?
        ),
        ExpressionKind::ArrayJoin { array, separator } => format!(
            "{}.join({}.as_str())",
            emit_borrow_source(array)?,
            emit_borrow_source(separator)?
        ),
        ExpressionKind::OptionCheck { value, is_some } => format!(
            "{}.{}()",
            emit_borrow_source(value)?,
            if *is_some { "is_some" } else { "is_none" }
        ),
        ExpressionKind::OptionUnwrapOr { value, fallback } => format!(
            "{}.unwrap_or({})",
            emit_expression(value)?,
            emit_expression(fallback)?
        ),
        ExpressionKind::ResultCheck { value, is_ok } => format!(
            "{}.{}()",
            emit_borrow_source(value)?,
            if *is_ok { "is_ok" } else { "is_err" }
        ),
        ExpressionKind::ResultUnwrapOr { value, fallback } => format!(
            "{}.unwrap_or({})",
            emit_expression(value)?,
            emit_expression(fallback)?
        ),
        ExpressionKind::StringMethod {
            receiver,
            method,
            argument,
        } => format!(
            "{}.{}({}.as_str())",
            emit_borrow_source(receiver)?,
            match method {
                StringMethod::Includes => "contains",
                StringMethod::StartsWith => "starts_with",
                StringMethod::EndsWith => "ends_with",
            },
            emit_borrow_source(argument)?
        ),
        ExpressionKind::StringTransform {
            receiver,
            transform,
        } => match transform {
            StringTransform::Trim => {
                format!("{}.trim().to_string()", emit_borrow_source(receiver)?)
            }
            StringTransform::ToUpperCase => {
                format!("{}.to_uppercase()", emit_borrow_source(receiver)?)
            }
            StringTransform::ToLowerCase => {
                format!("{}.to_lowercase()", emit_borrow_source(receiver)?)
            }
        },
        ExpressionKind::EnumVariant {
            enumeration,
            variant,
        } => format!(
            "{}::{}",
            rust_type_identifier(enumeration),
            rust_type_identifier(variant)
        ),
        ExpressionKind::Binary {
            left,
            operator,
            right,
        } => {
            if *operator == BinaryOperator::Add && expression.ty == Type::String {
                format!(
                    "format!(\"{{}}{{}}\", {}, {})",
                    emit_borrow_source(left)?,
                    emit_borrow_source(right)?
                )
            } else {
                let precedence = binary_precedence(*operator);
                let output = format!(
                    "{} {} {}",
                    emit_expression_with_precedence(left, precedence)?,
                    emit_operator(*operator),
                    emit_expression_with_precedence(right, precedence + 1)?
                );
                if precedence < parent_precedence {
                    format!("({output})")
                } else {
                    output
                }
            }
        }
        ExpressionKind::Unary { operator, value } => {
            format!(
                "{}({})",
                emit_unary_operator(*operator),
                emit_expression(value)?
            )
        }
        ExpressionKind::Conditional {
            condition,
            then_value,
            else_value,
        } => format!(
            "if {} {{ {} }} else {{ {} }}",
            emit_expression(condition)?,
            emit_expression(then_value)?,
            emit_expression(else_value)?
        ),
        ExpressionKind::Template { parts, expressions } => {
            if parts.len() != expressions.len() + 1 {
                return Err(CodegenError::InvalidIr(
                    "template parts must surround every expression".to_owned(),
                ));
            }
            let format_string =
                parts
                    .iter()
                    .enumerate()
                    .fold(String::new(), |mut output, (index, part)| {
                        output.push_str(&part.replace('{', "{{").replace('}', "}}"));
                        if index < expressions.len() {
                            output.push_str("{}");
                        }
                        output
                    });
            if expressions.is_empty() {
                format!("{format_string:?}.to_string()")
            } else {
                format!(
                    "format!({format_string:?}, {})",
                    expressions
                        .iter()
                        .map(emit_borrow_source)
                        .collect::<Result<Vec<_>, _>>()?
                        .join(", ")
                )
            }
        }
    })
}

fn emit_operator(operator: BinaryOperator) -> &'static str {
    match operator {
        BinaryOperator::Add => "+",
        BinaryOperator::Subtract => "-",
        BinaryOperator::Multiply => "*",
        BinaryOperator::Divide => "/",
        BinaryOperator::Equal => "==",
        BinaryOperator::NotEqual => "!=",
        BinaryOperator::Greater => ">",
        BinaryOperator::GreaterEqual => ">=",
        BinaryOperator::Less => "<",
        BinaryOperator::LessEqual => "<=",
        BinaryOperator::And => "&&",
        BinaryOperator::Or => "||",
        BinaryOperator::Remainder => "%",
    }
}

fn binary_precedence(operator: BinaryOperator) -> u8 {
    match operator {
        BinaryOperator::Or => 1,
        BinaryOperator::And => 2,
        BinaryOperator::Equal | BinaryOperator::NotEqual => 3,
        BinaryOperator::Greater
        | BinaryOperator::GreaterEqual
        | BinaryOperator::Less
        | BinaryOperator::LessEqual => 4,
        BinaryOperator::Add | BinaryOperator::Subtract => 5,
        BinaryOperator::Multiply | BinaryOperator::Divide | BinaryOperator::Remainder => 6,
    }
}

fn emit_unary_operator(operator: UnaryOperator) -> &'static str {
    match operator {
        UnaryOperator::Not => "!",
        UnaryOperator::Negate => "-",
    }
}

fn rust_callee(name: &str) -> String {
    match name {
        "JSON.parse" => "rustify_runtime::json::parse".to_owned(),
        "JSON.stringify" => "rustify_runtime::json::stringify".to_owned(),
        "JSON.stringifyPretty" => "rustify_runtime::json::stringify_pretty".to_owned(),
        "Rustify.sleep" => "rustify_runtime::async_runtime::sleep".to_owned(),
        "Math.abs" => "f64::abs".to_owned(),
        "Math.floor" => "f64::floor".to_owned(),
        "Math.ceil" => "f64::ceil".to_owned(),
        "Math.round" => "f64::round".to_owned(),
        "Math.min" => "f64::min".to_owned(),
        "Math.max" => "f64::max".to_owned(),
        "Math.pow" => "f64::powf".to_owned(),
        "Ok" | "Err" => name.to_owned(),
        _ => rust_identifier(name),
    }
}

pub fn rust_identifier(name: &str) -> String {
    let mut output = String::new();
    for (index, character) in name.chars().enumerate() {
        if character.is_ascii_uppercase() {
            if index > 0 && !output.ends_with('_') {
                output.push('_');
            }
            output.push(character.to_ascii_lowercase());
        } else if character == '$' {
            output.push_str("_dollar_");
        } else {
            output.push(character);
        }
    }
    if matches!(
        output.as_str(),
        "as" | "break"
            | "const"
            | "continue"
            | "crate"
            | "else"
            | "enum"
            | "extern"
            | "false"
            | "fn"
            | "for"
            | "if"
            | "impl"
            | "in"
            | "let"
            | "loop"
            | "match"
            | "mod"
            | "move"
            | "mut"
            | "pub"
            | "ref"
            | "return"
            | "self"
            | "static"
            | "struct"
            | "super"
            | "trait"
            | "true"
            | "type"
            | "unsafe"
            | "use"
            | "where"
            | "while"
    ) {
        format!("r#{output}")
    } else {
        output
    }
}

pub fn rust_module_identifier(name: &str) -> String {
    let sanitized = name
        .chars()
        .map(|character| {
            if character.is_ascii_alphanumeric() || character == '_' {
                character
            } else {
                '_'
            }
        })
        .collect::<String>();
    let identifier = rust_identifier(&sanitized);
    let identifier = identifier.strip_prefix("r#").unwrap_or(&identifier);
    if identifier.is_empty() {
        "rustify_module".to_owned()
    } else {
        format!("rustify_{identifier}")
    }
}

pub fn rust_type_identifier(name: &str) -> String {
    let mut output = String::new();
    let mut capitalize = true;

    for character in name.chars() {
        if character == '$' {
            output.push_str("Dollar");
            capitalize = true;
        } else if character == '_' {
            capitalize = true;
        } else if capitalize {
            output.extend(character.to_uppercase());
            capitalize = false;
        } else {
            output.push(character);
        }
    }

    if output == "Self" {
        "RustifySelf".to_owned()
    } else {
        output
    }
}

